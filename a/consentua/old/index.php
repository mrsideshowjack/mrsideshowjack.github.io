        <h2>Introduction</h2>

        <p>Consentua is a <strong>consent management system</strong> from KnowNow Information that helps organisations to achieve data protection compliance, and gives individuals choice and control over how
        their personal data is used.</p>
        
        <p>Consentua is currently in <strong>closed beta</strong> but get in touch to find out more.</p>

        <img src="img/collection.png"  alt="Consent Collection" />
    </div>

    <div class="feature right">
        <h3>Subject Control</h3>
        <p>Consentua gives individual data subjects control over their personal data, allowing them to feel safe when engaging with digital services.  
        Consentua's dynamic tokens, coupled with a user-friendly dashboard, allow consent to be queried, changed and even revoked.</p>
        
        <img src="img/dashboard.png"  alt="Consent Dashboard" />
    </div>

    <div class="feature">
        <h3>Compliance</h3>
        <p>When it comes to complying with the new GDPR requirements around data-subject consent, Consentua could be your secret weapon.  Collect and record consent with tested 
        interaction mechanisms, query consent in real-time to detect revocation and use the provided audit trails to demonstrate the consent that your business relies on.</p>
        
        <img src="img/gdpr.png"  alt="GDPR Compliance" />
    </div>

    <div class="feature right">
        <h3>Trust</h3>
        <p>Consentua helps organisations to build consumer trust by providing data processing transparency and giving data subjects the choice and control that they need in order to share
        data safely and comfortably.</p>
        
        <img src="img/control.png"  alt="Data subject consent control" />
    </div>

